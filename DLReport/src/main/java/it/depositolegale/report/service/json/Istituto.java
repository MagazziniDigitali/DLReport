package it.depositolegale.report.service.json;

public class Istituto {
	private String nome;
	private String sigla;

	public Istituto() {
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getSigla() {
		return sigla;
	}

	public void setSigla(String sigla) {
		this.sigla = sigla;
	}

}
